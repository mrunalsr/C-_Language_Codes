//Data types and sizes
#include<iostream>
using namespace std;
int main()
{
    int a = 10;
    float b = 12.3456;
    char c = 'C';
    double d = 12.34567890;
    cout<<"Value of int a is : "<<a<<endl;
    cout<<"The size of int data type is : "<<sizeof(a)<<"Bytes"<<endl;
    cout<<"Value of float b is : "<<b<<endl;
    cout<<"The size of float data type is : "<<sizeof(b)<<"Bytes"<<endl;
    cout<<"Value of char c is : "<<c<<endl;
    cout<<"The size of char c is : "<<sizeof(c)<<"Bytes"<<endl;
    cout<<"Value of double d is : "<<d<<endl;
    cout<<"The size of double data type is : "<<sizeof(d)<<"Bytes"<<endl;
    
    return 0;
}